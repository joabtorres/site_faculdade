		<h4 class="text-justify text-blue text-uppercase">
		    história
		</h4>
		<p class="text-justify text-indent">
		    Os líderes da Comunidade, Paróquia e os representantes do município de Novo Progresso, ao pensarem a criação das Faculdades Católicas Cavanis, discutiram, analisaram e estudaram a viabilidade de execução deste projeto. 
		</p>
		<p class="text-justify text-indent">
		    Desde 2004, um grupo de representantes das lideranças locais, reunidas com o Pe. Giuseppe Viani, então superior Provincial da Província Antônio e Marcos Cavanis do Brasil, atendendo ao projeto da Congregação de realizar uma obra educativa no espírito dos nossos Fundadores, Pe. Antônio e Pe. Marcos Cavanis, em Novo Progresso, foram feitas algumas reuniões com vistas à efetivação deste objetivo. Foi porem, a partir de 2007, com a vinda do Pe. Giuseppe Viani para a paróquia Santa Luzia de Novo Progresso – PA, que iniciou a concretização do nosso sonho: montar uma Faculdade na Amazônia.
		</p>
		<p class="text-justify text-indent">
		    Assim, foram muitas reuniões e aos poucos nasceu a Sociedade Educacional Católica do Oeste do Pará (SECOP), com os estatutos, os sócios fundadores, a diretoria e o CNPJ. A Divina Providência permitiu que, com o recurso de muita gente, conseguíssemos adquirir uma área urbana de terra com mais de 150.000 metros quadrados. Os sócios e diretoria discutiram sobre os cursos a serem implantados. Prevaleceu à opinião de se priorizar cursos superiores úteis para a nossa região. 
		</p>
		<p class="text-justify text-indent">
		    Desde o início nos perguntamos: que tipo Faculdade pretendiamos criar? O próprio nome explica o rosto que pretendemos dar a partir de seus planos e projeto. Ela é: “Faculdade”: sua preocupação é com a qualidade de ensino, com o aproveitamento e a capacitação dos alunos, e com a pesquisa. Pretende exercer um papel de serviço e colaboração ao Brasil e ao mundo. É “Católica”: no significado literal da palavra é aberta a todos, especialmente aos que mais precisam, e situada numa região de grande necessidade. Ela defende e promove os princípios e os valores fundamentais do cristianismo: a família, a justiça, o mútuo respeito e a liberdade de cada um, a colaboração e ajuda entre os povos e construção de um mundo de paz e de vida para todos. É “Cavanis”: exige que o educador seja presente, trabalhe com espírito de gratidão e de gratuidade, acredite e ame o que faz, aprimore constantemente sua competência, e em conjunto com toda a instituição faça da Faculdade Católica Cavanis, uma família.
		</p>
		<h4 class="text-justify text-blue text-uppercase">
		    missão
		</h4>
		<p class="text-justify text-indent">
		    A Faculdade Católica Cavanis do Sudoeste do Pará tem como missão, produzir, sistematizar e socializar o conhecimento a partir de valores ético-cristãos, tendo como perspectiva o desenvolvimento de uma sociedade justa, democrática e solidária. Por meio de um ensino de excelência e a necessária indissociabilidade entre iniciação a pesquisa e extensão, a Faculdade incentiva a produção de novos conhecimentos e preparam profissionais a partir de sustentável base teórica e coerência entre a teoria e a prática e contribui para o avanço educacional, científico, tecnológico e cultural, pautando-se no respeito ao pluralismo e à diversidade, na participação ativa, corresponsável e no compromisso socioeconômico e ambiental da região sudoeste do Pará. Com empenho, comprometimento e espírito comunitário oportuniza uma educação de qualidade na formação profissional e de especialistas em nível superior, incluindo a formação ética, o desenvolvimento da autonomia intelectual e do pensamento crítico e reflexivo comprometida com a dignidade humana, nos aspectos espirituais, sociais e afetivos, alicerçados nos sólidos princípios do Evangelho.
