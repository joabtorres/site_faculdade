		<p class="text-justify text-indent">
		O curso de Especialização em Docência no Ensino Superior tem seu Projeto Pedagógico de Curso (PPC) construído em consonância com as necessidades da formação de profissionais em nível de pós-graduação Lato Sensu. Tem a sua concepção voltada para a prática docente, sendo um de seus grandes diferenciais. A Proposta ora apresentada trabalhará as várias dimensões do processo de “tornar-se professor”. Todas as áreas de educação necessitam de um sistema aprimorado de formação de seus professores que permita a atualização dos conhecimentos, desenvolva as competências pedagógicas próprias para o nível de ensino e leve em conta os meandros e peculiaridades do setor em suas proposições educacionais. Nesse sentido, faz-se necessária a formação de docentes para atuação no nível superior com competências pedagógicas que lhe sustentem a prática. O curso busca proporcionar a construção do conhecimento do pós-graduando preparando-o profissionalmente para os desafios do mercado de trabalho, especialmente no que tange a compreensão e entendimento do fazer pedagógico inerente ao ensino superior, bem como a gerência desse espaço de educação, oportunizando a esses educandos uma visão crítica acerca das exigências legais, teóricas e práticas da profissão de educador do ensino superior e ainda o entendimento da gestão de cursos de graduação. 
		
